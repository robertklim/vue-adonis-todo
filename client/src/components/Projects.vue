<template>
    <Panel title="Projects">
        <div
            class="project mt-2"
            v-for="project in projects"
            :key="project.id"    
        >
            <v-layout row wrap>
                <v-flex xs9 class="text-xs-left">
                    <span 
                        v-if="!project.isEditMode"
                    >
                        {{ project.title }}
                    </span>
                    <v-text-field 
                        autofocus
                        v-if="project.isEditMode"
                        :value="project.title"
                        @keyup.enter="saveProject(project)"
                        @input="setProjectTitle({
                            project,
                            title: $event,
                        })"
                    ></v-text-field>
                </v-flex>
                <v-flex xs3>
                    <v-icon
                        v-if="!project.isEditMode"
                        @click="setEditMode(project)">
                        edit
                    </v-icon>
                    <v-icon
                        v-if="project.isEditMode"
                        @click="saveProject(project)">
                        check
                    </v-icon>
                    <v-icon
                        @click="deleteProject(project)">
                        delete
                    </v-icon>
                </v-flex>
            </v-layout>
        </div>
        <CreateRecord 
            placeholder="Project name..."
            @onInput="setNewProjectName"
            :value="newProjectName"
            @create="createProject"
        />
    </Panel>
</template>

<script>
import { mapActions, mapMutations, mapState } from 'vuex';
import CreateRecord from '@/components/CreateRecord.vue';
export default {
    mounted() {
        this.fetchProjects();
    },
    components: {
        CreateRecord,
    },
    computed: {
        ...mapState('projects', [
            'newProjectName',
            'projects',
        ]),
    },
    methods: {
        ...mapMutations('projects', [
            'setNewProjectName',
            'setEditMode',
            'unsetEditMode',
            'setProjectTitle',
        ]),
        ...mapActions('projects', [
            'createProject',
            'fetchProjects',
            'saveProject',
            'deleteProject',
        ]),
    },
};
</script>

<style>

    .project {
        font-size: 1.2em;
    }

    .v-icon:hover {
        color: #333;
    }

</style>
